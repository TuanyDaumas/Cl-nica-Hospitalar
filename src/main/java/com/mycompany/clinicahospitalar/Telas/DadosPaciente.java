    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
         // Verifica se alguma linha da tabela está selecionada
    int linhaSelecionada = tblPaciente.getSelectedRow();
    if (linhaSelecionada == -1) {
        // Se nenhuma linha estiver selecionada, exibe uma mensagem de erro e encerra o método
        JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada.", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Pergunta ao usuário se ele tem certeza de que deseja excluir a linha selecionada
    int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir esta linha?", "Confirmação", JOptionPane.YES_NO_OPTION);
    if (opcao == JOptionPane.YES_OPTION) {
        // Se o usuário confirmar a exclusão, remove o paciente da lista e da tabela
        listaPaciente.remove(linhaSelecionada); // Remove o paciente da lista usando o índice da linha selecionada
        DefaultTableModel modeloTabela = (DefaultTableModel) tblPaciente.getModel(); // Obtém o modelo da tabela
        modeloTabela.removeRow(linhaSelecionada); // Remove a linha da tabela usando o índice da linha selecionada
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
     // Verifica se alguma linha foi selecionada na tabela
    int linhaSelecionada = tblPaciente.getSelectedRow();
    if (linhaSelecionada == -1) {
        JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada.", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Obtém o paciente selecionado na tabela
    Paciente pacienteSelecionado = listaPaciente.get(linhaSelecionada);

    // Obtém os novos valores dos campos de texto
    String novoNome = JOptionPane.showInputDialog(this, "Digite o novo nome:", pacienteSelecionado.getNome());
    String novaDataNascimentoTexto = JOptionPane.showInputDialog(this, "Digite a nova data de nascimento (dd/mm/yyyy):", pacienteSelecionado.getDataNascimento());
    // Adicione aqui mais solicitações de entrada de dados para outros campos, se necessário

    // Verifica se o usuário cancelou a operação
    if (novoNome == null || novaDataNascimentoTexto == null) {
        return; // Cancela a operação
    }

    // Tenta atualizar os dados do paciente com os novos valores
    try {
        // Atualiza o nome do paciente
        pacienteSelecionado.setNome(novoNome);
        // Atualiza a data de nascimento do paciente
        DateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");
        Date novaDataNascimento = formatoData.parse(novaDataNascimentoTexto);
        pacienteSelecionado.setDataNascimento(novaDataNascimento);
        // mais atualizações ...

        // Atualiza a linha na tabela
        tblPaciente.setValueAt(novoNome, linhaSelecionada, 1); // Atualiza o nome na segunda coluna (índice 1)
        tblPaciente.setValueAt(novaDataNascimentoTexto, linhaSelecionada, 2); // Atualiza a data de nascimento na terceira coluna (índice 2)
        // mais atualizações ...

        // Exibe mensagem de sucesso
        JOptionPane.showMessageDialog(this, "Dados atualizados com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    } catch (ParseException ex) {
        JOptionPane.showMessageDialog(this, "Formato de data inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnEditarActionPerformed

