    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        try {
        // Extrair dados dos campos de texto
        String nome = txtNome.getText();
        String dataNascimentoTexto = txtNasc.getText();
        DateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");
        Date nasc = formatoData.parse(dataNascimentoTexto);
        int idade = Integer.parseInt(txtIdade.getText());
        String genero = rbMasculino.isSelected() ? "Masculino" : "Feminino";
        String dataCadastroTexto = txtDataCadastro.getText();
        Date dataCadastro = formatoData.parse(dataCadastroTexto);
        
        String telefone = txtTel.getText();
        String celular = txtCel.getText();
        String email = txtEmail.getText();
        
        String contatoResponsavelTelefone = txtContatoResponsavelTelefone.getText(); //  telefone do contato responsável
        String contatoResponsavelCelular = txtContatoResponsavelCelular.getText(); // celular do contato responsável
        String contatoResponsavelEmail = txtContatoResponsavelEmail.getText(); // email do contato responsável
        
        String rua = txtRua.getText();
        String num = txtNum.getText();
        String bairro = txtBairro.getText();
        String cidade = txtCidade.getText();
        String cep = txtCep.getText();
        String estado = txtEstado.getText();
        String obsGeral = txtObs.getText();
        
     
        // Verificar campos obrigatórios foram preenchidos
        if (nome.isEmpty() || dataNascimentoTexto.isEmpty() || genero.isEmpty() || celular.isEmpty() || 
                cidade.isEmpty() || estado.isEmpty() || dataCadastroTexto.isEmpty()) {
            throw new IllegalArgumentException("Os campos obrigatórios não foram preenchidos.");
        }
        
        // Verificar se os campos de telefone, celular, Contato Responsável, número e CEP contêm apenas números
        if (!cep.matches("\\d+") || !contatoResponsavelTelefone.matches("\\d+") 
                || !telefone.matches("\\d+") || !celular.matches("\\d+") || !num.matches("\\d+")) {
            throw new IllegalArgumentException("Os campos de telefone, celular, Contato Responsável, número e CEP devem conter apenas números.");
        }

        // Convertendo o idPaciente para Long
        Long idPacienteLong = Long.valueOf(idPaciente); 

        // Gerar um novo ID único para a consulta médica
        Long idConsulta = gerarIdUnicoConsulta();

        // Inicializar a lista de consultas médicas
        List<ConsultaMedica> historicoConsultasMedicas = new ArrayList<>();
        
        // Criar um novo objeto ContatoTelEmail com os dados dos campos de texto
        ContatoTelEmail contatoResponsavel = new ContatoTelEmail(contatoResponsavelTelefone, contatoResponsavelCelular, contatoResponsavelEmail);

        // Construir um novo Paciente com o objeto ContatoTelEmail criado
        Paciente novoPaciente = new Paciente(nome, nasc, new Endereco(rua, Integer.parseInt(num), bairro, cidade, estado, Integer.parseInt(cep)), contatoResponsavel, Genero.fromString(genero), idPacienteLong, idade, dataCadastro, obsGeral, historicoConsultasMedicas, contatoResponsavel);

        // Criar e inicializar uma nova consulta médica
        ConsultaMedica novaConsultaMedica = new ConsultaMedica(
            idConsulta,  // ID único gerado para a consulta
            novoPaciente,  // Paciente associado à consulta
            null,  // Médico responsável pela consulta 
            null,  // Descrição do exame e queixa do paciente 
            null,  // Diagnóstico médico resultante da consulta 
            null,  // Prescrição médica resultante da consulta
            false  // Indicador de indicação cirúrgica 
        );

        // Adicionar nova consulta médica ao histórico
        novoPaciente.addConsultaMedica(novaConsultaMedica);

        // Adicionar à lista
        listaPaciente.add(novoPaciente);

        // Adicionar à tabela
        adicionarPacienteATabela(novoPaciente);

        // Incrementar o ID único para o próximo médico
        idPaciente++;
        
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, 
                "Certifique-se de preencher corretamente os campos numéricos.", "Erro", JOptionPane.ERROR_MESSAGE);
    } catch (IllegalArgumentException ex) {
        JOptionPane.showMessageDialog(this, 
                ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    } catch (ParseException ex) {
        JOptionPane.showMessageDialog(this, 
                "Formato de data inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnCadastrarActionPerformed

