    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:                                      
        // Método chamado quando o botão "Cadastrar" é clicado
    try {
        // Extrai dados dos campos de texto da interface
        String nome = txtNome.getText();
        String dataNascimentoTexto = txtNasc.getText(); // Obtém o texto do campo de texto
        String genero = rbMasculino.isSelected() ? "Masculino" : "Feminino";
        String tel = txtTel.getText();
        String cel = txtCel.getText();
        String email = txtEmail.getText();
        String rua = txtRua.getText();
        String num = txtNum.getText();
        String bairro = txtBairro.getText();
        String cidade = txtCidade.getText();
        String cep = txtCep.getText();
        String estado = txtEstado.getText();
        String setor = txtSetor.getText();
        String ch = txtCh.getText(); 

        // Verifica campos obrigatórios
        if (nome.isEmpty() || dataNascimentoTexto.isEmpty() || genero.isEmpty() || cel.isEmpty() 
                || cidade.isEmpty() || cep.isEmpty() || estado.isEmpty() || setor.isEmpty() || ch.isEmpty()) {
            throw new IllegalArgumentException("Os campos obrigatórios não foram preenchidos.");
        }

        // Verifica se os campos de telefone, celular, número e CEP contêm apenas números
        if (!tel.matches("\\d+") || !cel.matches("\\d+") || !num.matches("\\d+") 
                || !cep.matches("\\d+") || !ch.matches("\\d+")) {
            throw new IllegalArgumentException("Os campos de telefone, celular, número, CEP e Carga "
                    + "Horaria devem conter apenas números.");
        }

        // Define o formato da data
        DateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");

        // Utiliza try-catch para lidar com a possível exceção ParseException
        Date nasc;
        try {
            nasc = formatoData.parse(dataNascimentoTexto); // Converte o texto para um objeto Date
        } catch (ParseException ex) {
            // Exibe mensagem de erro se houver erro no formato da data
            JOptionPane.showMessageDialog(this, "Formato de data inválido. Use o formato dd/MM/yyyy.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return; // Sai do método após exibir a mensagem de erro
        }

        // Convertendo o idAtendente para Long
        Long idAtendenteLong = Long.valueOf(idAtendente);

        // Cria um novo objeto AtendenteHospitalar com os dados fornecidos
        AtendenteHospitalar novoAtendente = new AtendenteHospitalar(nome, nasc, 
    new Endereco(rua, Integer.parseInt(num), bairro, cidade, estado, Integer.parseInt(cep)), 
    new ContatoTelEmail(tel, cel, email), 
    Genero.fromString(genero), setor, Integer.parseInt(ch), idAtendente);

        // Adiciona o atendente à lista
        listaAtendenteHospitalar.add(novoAtendente);

        // Adiciona o atendente à tabela
        adicionarAtendenteATabela(novoAtendente);

        // Incrementa o ID único para o próximo atendente
        idAtendente++;
    } catch (IllegalArgumentException ex) {
        // Se houver exceção devido a campos vazios ou inválidos, exibe mensagem de erro
        JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnCadastrarActionPerformed

