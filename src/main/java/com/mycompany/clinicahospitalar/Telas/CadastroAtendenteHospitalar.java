    private void adicionarAtendenteATabela(AtendenteHospitalar atendente) {
        DefaultTableModel modeloTabela = (DefaultTableModel) tblAtendente.getModel();

        // Adiciona todos os dados do atendente ao array de dados
        Object[] dados = {atendente.getNomeCompleto(), atendente.getDataNascimento(), atendente.getGenero(),
                atendente.getContato().getTelefone(), atendente.getContato().getCelular(),
                atendente.getContato().getEmail(), atendente.getEndereco().getRua(),
                atendente.getEndereco().getNumero(), atendente.getEndereco().getBairro(),
                atendente.getEndereco().getCidade(), atendente.getEndereco().getCep(),
                atendente.getEndereco().getEstado(), atendente.getSetor(), atendente.getChSemanal()};

        // Adiciona linha à tabela
        modeloTabela.addRow(dados);
    }


    
