    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        // Verifica se alguma linha da tabela está selecionada
        int linhaSelecionada = tblAtendente.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Nenhuma linha selecionada.", 
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Obtém o modelo da tabela
        DefaultTableModel modeloTabela = (DefaultTableModel) tblAtendente.getModel();

        // Atualiza os dados da linha selecionada com os novos valores dos campos editados
        for (int i = 0; i < modeloTabela.getColumnCount(); i++) {
            Object novoValor = modeloTabela.getValueAt(linhaSelecionada, i); // Obtém o novo valor do campo editado
            modeloTabela.setValueAt(novoValor, linhaSelecionada, i); // Atualiza o valor na tabela
        }

        // Exibe mensagem de confirmação
        JOptionPane.showMessageDialog(this, "As alterações foram salvas com sucesso. Clique fora da Tabela.", 
                "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSalvarActionPerformed

