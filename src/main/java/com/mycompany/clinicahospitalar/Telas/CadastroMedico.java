    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        // Método chamado quando o botão "Cadastrar" é clicado
        try {
            // Extrai dados dos campos de texto
            String nome = txtNome.getText();
            String dataNascimentoTexto = txtNasc.getText(); // Obtém o texto do campo de texto
            DateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy"); // Define o formato da data
            Date nasc = formatoData.parse(dataNascimentoTexto); // Converte o texto para um objeto Date
            String genero = rbMasculino.isSelected() ? "Masculino" : "Feminino";
            String tel = txtTel.getText();
            String cel = txtCel.getText();
            String email = txtEmail.getText();
            String rua = txtRua.getText();
            String num = txtNum.getText();
            String bairro = txtBairro.getText();
            String cidade = txtCidade.getText();
            String cep = txtCep.getText();
            String estado = txtEstado.getText();
            int idade = Integer.parseInt(txtIdade.getText()); // Extrai a idade do campo de texto
            boolean cir = rbS.isSelected();
        

            // Verifica campos obrigatórios
            if (nome.isEmpty() || dataNascimentoTexto.isEmpty() || genero.isEmpty() || cel.isEmpty() || cidade.isEmpty() || cep.isEmpty() || estado.isEmpty()) {
            throw new IllegalArgumentException("Os campos obrigatórios não foram preenchidos.");
        }

            // Verifica se os campos de telefone, celular, número e CEP contêm apenas números
            if (!tel.matches("\\d+") || !cel.matches("\\d+") || !num.matches("\\d+") || !cep.matches("\\d+")) {
            throw new IllegalArgumentException("Os campos de telefone, celular, número e CEP devem conter apenas números.");
        }

            // Criar lista de especialidades
            List<String> especialidades = new ArrayList<>();
            // Adicionar especialidades conforme necessário
            especialidades.add("Cardiologia");
            especialidades.add("Pediatria");
            especialidades.add("Clinico");
    //mais especialidades....

    // Convertendo o idMedico para Long
    Long idMedicoLong = Long.valueOf(idMedico);

    // Chamada do construtor Medico com idMedicoLong e idade extraída do campo de texto
    Medico novoMedico = new Medico(nome, nasc, new Endereco(rua, Integer.parseInt(num), bairro, cidade, estado, Integer.parseInt(cep)), new ContatoTelEmail(tel, cel, email), Genero.fromString(genero), idMedicoLong, idade, especialidades, cir);

    // Adiciona o médico à lista
    listaMedico.add(novoMedico);

    // Adiciona o médico à tabela
    adicionarMedicoATabela(novoMedico);

    // Incrementa o ID único para o próximo médico
    idMedico++;
} catch (IllegalArgumentException ex) {
    JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
} catch (ParseException ex) {
    JOptionPane.showMessageDialog(this, "Formato de data inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_btnCadastrarActionPerformed

